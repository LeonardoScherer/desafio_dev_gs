<script>
    function checkPasswordStrength() {
        var numbers = /([0-9])/;
        var alphabet = /([a-zA-Z])/;
        var chSpecials = /([~,!,@,#,$,%,^,&,*,-,_,+,=,?,>,<])/;

        var strong = <?php echo json_encode(__('messages.Strong')); ?>;
        var weak = <?php echo json_encode(__('messages.pass_weak')); ?>;
        var medium = <?php echo json_encode(__('messages.pass_medium')); ?>;

        if($('#password').val().length < 6)
        {
            $('#password-strength').html("<span style='color:red'>" + weak + "</span>");
        } else {
            if($('#password').val().match(numbers) && $('#password').val().match(alphabet) && $('#password').val().match(chSpecials)) {
                $('#password-strength').html("<span style='color:green'><b>" + strong + "</b></span>");
            } else {
                $('#password-strength').html("<span style='color:orange'>" + medium + "</span>");
            }
        }
    }
</script>
